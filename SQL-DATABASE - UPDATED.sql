-- TABLE CREATION

CREATE TABLE attractions
	(attractionID		INTEGER	NOT NULL AUTO_INCREMENT,
     attractionName		VARCHAR(20) NOT NULL,
     ticketPrice		DECIMAL(10, 2) NOT NULL,
     CONSTRAINT attractions_pk PRIMARY KEY (attractionID));
ALTER TABLE attractions AUTO_INCREMENT = 111;

CREATE TABLE visitors
	(visitorID			INTEGER NOT NULL AUTO_INCREMENT,
     last_name			VARCHAR(20) NOT NULL,
     first_name			VARCHAR(20) NOT NULL,
     CONSTRAINT visitors_pk PRIMARY KEY (visitorID));
ALTER TABLE visitors AUTO_INCREMENT = 001;

CREATE TABLE tickets
	(ticketID		INTEGER NOT NULL AUTO_INCREMENT,
     use_date		DATE,
     CONSTRAINT tickets_pk PRIMARY KEY (ticketID));
ALTER TABLE tickets AUTO_INCREMENT = 101;

CREATE TABLE staff
	(staffID		INTEGER NOT NULL AUTO_INCREMENT,
     last_name		VARCHAR(20) NOT NULL,
     first_name		VARCHAR(20) NOT NULL,
     emp_date		DATE NOT NULL,
     salary			DECIMAL(10, 2) NOT NULL,
     CONSTRAINT staff_pk PRIMARY KEY (staffID));
ALTER TABLE staff AUTO_INCREMENT = 1001;

CREATE TABLE transactions
	(transID		INTEGER NOT NULL AUTO_INCREMENT,
     staffID		INTEGER NOT NULL,
     ticketID		INTEGER NOT NULL UNIQUE,
     attractionID	INTEGER NOT NULL,
     visitorID		INTEGER NOT NULL,
     purchase_date	DATE NOT NULL,
     CONSTRAINT transactions_pk PRIMARY KEY (transID),
     CONSTRAINT transactions_fk1 FOREIGN KEY (staffID) REFERENCES staff (staffID) ON DELETE CASCADE,
     CONSTRAINT transactions_fk2 FOREIGN KEY (ticketID) REFERENCES tickets (ticketID) ON DELETE CASCADE,
     CONSTRAINT transactions_fk3 FOREIGN KEY (attractionID) REFERENCES attractions (attractionID) ON DELETE CASCADE,
	 CONSTRAINT transactions_fk4 FOREIGN KEY (visitorID) REFERENCES visitors (visitorID) ON DELETE CASCADE);
ALTER TABLE transactions AUTO_INCREMENT = 10111;

CREATE TABLE assignments
	(assignmentID		INTEGER NOT NULL AUTO_INCREMENT,
     staffID			INTEGER NOT NULL,
     attractionID		INTEGER NOT NULL,
     from_date			DATE NOT NULL,
     to_date			DATE,
     CONSTRAINT assignments_pk PRIMARY KEY (assignmentID),
     CONSTRAINT assignments_fk1 FOREIGN KEY (staffID) REFERENCES staff (staffID) ON DELETE CASCADE,
     CONSTRAINT assignments_fk2 FOREIGN KEY (attractionID) REFERENCES attractions (attractionID) ON DELETE CASCADE);
ALTER TABLE assignments AUTO_INCREMENT = 12001;

